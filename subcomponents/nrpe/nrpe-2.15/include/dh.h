#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0xD8,0xE4,0x8D,0x5D,0xC1,0x44,0x7F,0xA4,0x9D,0xA8,0x35,0x04,
		0x1B,0xD3,0xA6,0x57,0xB5,0xBD,0x22,0x85,0x33,0xB5,0x1F,0x1D,
		0x41,0x06,0x27,0x7B,0xD7,0xC5,0x8E,0x6E,0xC3,0xBD,0x15,0xB4,
		0xEE,0x56,0x0D,0x50,0xE5,0x34,0xF0,0xC6,0x24,0x21,0x40,0x06,
		0xAF,0xEE,0x24,0xD1,0x06,0x2F,0xFD,0x9D,0x5D,0xEF,0x02,0x6B,
		0x84,0x7A,0x83,0x5B,
		};
	static unsigned char dh512_g[]={
		0x02,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
